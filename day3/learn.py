# Operators

# Operators are used to perform operations on variables and values
# Python divides the operators in the following groups:
# 1. Arithmetic operators - Used to perform mathematical operations like addition, subtraction, multiplication, etc.
# 2. Assignment operators - Assign values to variables
# 3. Comparison operators - Compare two values and return a boolean result(True or False)
# 4. Logical operators - Used to combine conditional statements
# 5. Membership operators - Used to test if a sequence is presented in an object like is, in, not in, is not
